/**
 * Kelas Person merepresentasikan entitas pengguna dengan atribut username dan password.
 */
class Person {
    private String username;
    private String password;

    /**
     * Konstruktor untuk membuat objek Person baru dengan username dan password yang diberikan.
     * 
     * @param username nama pengguna
     * @param password kata sandi
     */
    public Person(String username, String password) {
        this.username = username;
        this.password = password;
    }

    /**
     * Mengembalikan username dari pengguna.
     * 
     * @return username
     */
    public String getUsername() {
        return username;
    }
    
    /**
     * Mengatur username pengguna.
     * 
     * @param username nama pengguna baru
     */
    public void setUsername(String username) {
        this.username = username;
    }
    
    /**
     * Mengembalikan password dari pengguna.
     * 
     * @return password
     */
    public String getPassword() {
        return password;
    }
    
    /**
     * Mengatur password pengguna.
     * 
     * @param password kata sandi baru
     */
    public void setPassword(String password) {
        this.password = password;
    }
}

/**
 * Kelas Student adalah subclass dari Person yang menambahkan atribut rePassword untuk konfirmasi kata sandi.
 */
class Student extends Person {
    private String rePassword;

    /**
     * Konstruktor untuk membuat objek Student baru dengan username, password, dan rePassword yang diberikan.
     * 
     * @param username nama pengguna
     * @param password kata sandi
     * @param rePassword konfirmasi kata sandi
     */
    public Student(String username, String password, String rePassword) {
        super(username, password);
        this.rePassword = rePassword;
    }

    /**
     * Mengembalikan rePassword dari pengguna.
     * 
     * @return rePassword
     */
    public String getRePassword() {
        return rePassword;
    }
    
    /**
     * Mengatur rePassword pengguna.
     * 
     * @param rePassword konfirmasi kata sandi baru
     */
    public void setRePassword(String rePassword) {
        this.rePassword = rePassword;
    }
}

