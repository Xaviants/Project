    /**
     * Metode ini dipanggil ketika tombol 'Login' ditekan.
     * Memproses data input dari pengguna dan melakukan validasi serta registrasi pengguna baru.
     * 
     * @param evt peristiwa yang dipicu oleh klik tombol Login
     */
    private void lgnBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_lgnBtnActionPerformed
        String usname = txtUsname.getText();
        String pw = new String(pwField.getPassword());
        String repw = new String(repwField.getPassword());
        
        Person person = new Student(usname, pw, repw); // Upcasting
        
        if(person instanceof Student){ //Cek InstanceOf
            Student student = (Student) person; //Downcasting
            
            if(student.getUsername().isEmpty() && student.getPassword().isEmpty() && student.getRePassword().isEmpty()){
                JOptionPane.showMessageDialog(this, "Username dan Password belum diisi", "WARNING", JOptionPane.WARNING_MESSAGE);
            }else{
                if(student.getUsername().isEmpty()){
                    JOptionPane.showMessageDialog(this, "Username belum diisi", "WARNING", JOptionPane.WARNING_MESSAGE);
                }else{
                    if(student.getPassword().isEmpty()){
                        JOptionPane.showMessageDialog(this, "Password belum diisi", "WARNING", JOptionPane.WARNING_MESSAGE);
                    }else{
                        if(student.getRePassword().isEmpty()){
                            JOptionPane.showMessageDialog(this, "Tuliskan kembali password pada kolom re-password", "WARNING", JOptionPane.WARNING_MESSAGE);
                        }else{
                            if(student.getPassword().length() < 8){
                                JOptionPane.showMessageDialog(this, "Panjang password harus minimal 8 karakter", "WARNING", JOptionPane.WARNING_MESSAGE);
                            }else{
                                if(student.getPassword().equals(student.getRePassword())){
                                    try{
                                        Statement state = ConnectionProvider.getConnected().createStatement();

                                        if(isUsnameTaken(usname)){
                                            JOptionPane.showMessageDialog(this, "Username ini sudah digunakan", "WARNING", JOptionPane.WARNING_MESSAGE);
                                        }else{
                                            state.executeUpdate("INSERT INTO data_username_password (username, password) values ('"+usname+"','"+pw+"')");
                                            JOptionPane.showMessageDialog(this, "Registrasi Berhasil", "INFORMASI", JOptionPane.INFORMATION_MESSAGE);

                                            this.setVisible(false);
                                            new LoginStudent().setVisible(true);

                                            reset();
                                        }
                                    }catch(Exception e){
                                        ExceptionError.errorMessage(e);
                                    }
                                }else{
                                    JOptionPane.showMessageDialog(this, "Password tidak sesuai", "WARNING", JOptionPane.WARNING_MESSAGE);
                                }
                            }
                        }
                    }
                }
            }
        }
    }//GEN-LAST:event_lgnBtnActionPerformed
    
