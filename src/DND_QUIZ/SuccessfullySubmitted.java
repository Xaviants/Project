    /**
     * Konstruktor untuk membuat instance baru dari kelas SuccessfullySubmitted
     * dengan parameter nilai akhir dari penggunaan tipe data String.
     * 
     * @param grade nilai akhir yang akan ditampilkan dalam pesan
     */
    public SuccessfullySubmitted(String grade) {
        initComponents();
        String finalGrade;
        int marksCompare = Integer.parseInt(grade);
        
        if(marksCompare < 70){
            finalGrade = "YAH! NILAI KAMU " + grade + ", BELAJAR LEBIH GIAT LAGI YA!";
        }else{
            finalGrade = "WOW! NILAI KAMU " + grade + ", PUH SEPUH!";
        }
        
        lblNilai.setText(finalGrade);
    }

